def predict_temperature_from_logits(logits, class_thresholds):
    """
    Convert ordinal logits into class labels and then map to temperature using thresholds.
    """
    # logits shape: (batch_size, num_classes - 1)
    probas = torch.sigmoid(logits)  # convert logits to probabilities
    pred_labels = torch.sum(probas > 0.5, dim=1)  # ordinal regression decoding
    pred_labels = pred_labels.cpu().numpy()

    # Map labels to temperature values using class thresholds
    pred_temps = []
    for label in pred_labels:
        if label == 0:
            pred_temps.append(class_thresholds[0])
        elif label == len(class_thresholds):
            pred_temps.append(class_thresholds[-1])
        else:
            # interpolate between thresholds
            temp = (class_thresholds[label - 1] + class_thresholds[label]) / 2
            pred_temps.append(temp)

    return np.array(pred_temps)

